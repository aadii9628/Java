public class data_types {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println(a+b);//int
		
		short c = 30;
		System.out.println(a+c);//int
		
		byte d=40;
		System.out.println(a+d);//int
		
		long l=2346;
		System.out.println(a+l);//int
		
		float f=23.4f;
		System.out.println(a+f);//float
		
		double D=23.56;
		System.out.println(a+D);//float
		System.out.println(D+f);//double
		
		char C='A';
		System.out.println(a+C);//int
		System.out.println(c+C);//int
		System.out.println(C+b);//int
		System.out.println(C+l);//int
		System.out.println(C+f);//float
		System.out.println(C+D);//float
	}
}
