//WAp for reverse right angle triangle

public class ReverseRightAngleTriangle{
    public static void main(String agrs[] ){

        for(int i=1;i<=4;i++)
        {
            for(int j=i;j<=4;j++)
            {
                System.out.print("* ");
            }
            System.out.print("\n");
        }
    }
}
